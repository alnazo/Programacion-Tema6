package bibliotecagranada;

public class Bibliotecario {
    String nombre;
    String dni;
    double sueldo;
    boolean finde;

    Bibliotecario(String nombre, String dni, double sueldo, boolean finde){
        this.nombre = nombre;
        this.dni = dni;
        this.sueldo = sueldo;
        this.finde = finde;
    }

}
